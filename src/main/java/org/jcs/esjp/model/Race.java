package org.jcs.esjp.model;

public class Race
{
    private String name;
    private String color;

    public String getName()
    {
        return name;
    }
    public void setName(final String name)
    {
        this.name = name;
    }
    public String getColor()
    {
        return color;
    }
    public void setColor(final String color)
    {
        this.color = color;
    }


}
